#include <iostream>
#include <string>
#include <bits/stdc++.h>

using namespace std;
//long long a[200010];


int main()
{
    int t;
    cin >> t;
    while(t--)
    {
        int n;
        int s=1;
        cin >> n;
        int a[n];

        for(int i=0; i<n; i++)
        {
            cin >> a[i];
        }

        for(int i=0; i<n; i++)
        {
            if(a[i]==1)
            {
                s+=1;
                if(a[i+1]==1)
                {
                    s+=4;
                }
            }
            else if(a[i]==0)
            {
                if(a[i+1]==0)
                {
                    s=-1;
                    break;
                }
            }
        }

        cout << s << endl;
    }

    return 0;
}
