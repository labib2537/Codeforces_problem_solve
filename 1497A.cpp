#include <iostream>
#include <string>
#include <bits/stdc++.h>
#include <algorithm>
using namespace std;
//long long a[200010];


int main()
{
    int t;
    cin >> t;
    while(t--)
    {
        int n;
		cin>>n;
		int arr[101]={0};
		for(int i=0;i<n;i++){
			int d;
			cin>>d;
			arr[d]+=1;
		}
		int i=0;
		while(n){
			if(arr[i]>=1){
				arr[i]--;
				cout<<i<<" ";
				n--;
			}
			i=(i+1)%101;
		}
		cout<<endl;


    }

    return 0;
}
