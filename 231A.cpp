int main()
{
    int n;
    cin >> n;
    int a,b,c;
    int cnt=0,flag=0;
    for(int i=0; i<n; i++)
    {
        cin >> a >> b >> c;
        if(a+b+c>1)
        {
            cnt++;
            flag=1;
        }
    }
    if(flag==1)
    {
        cout << cnt << endl;
    }
    else
    {
        cout << 0 << endl;
    }


    return 0;
}
