#include <iostream>
#include <string>
#include <bits/stdc++.h>

using namespace std;
//long long a[200010];


int main()
{
    int t;

    cin >> t;
    while(t--)
    {
        string s;
        cin>>s;
        for(int i=0; i<s.length(); i++)
        {
            if((i+1)%2==1)
            {
                if(s[i]!='a')
                {
                    s[i]='a';
                }

                else
                {
                    s[i]='b';
                }
            }
            else
            {
                if(s[i]!='z')
                {
                    s[i]='z';
                }
                else
                {
                    s[i]='y';
                }
            }
        }

        cout<<s<<endl;



    }
}
